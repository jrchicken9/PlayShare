#!/usr/bin/env node
/**
 * Regenerates server/playshare-extension-primer.auto.md and .meta.json from the repo.
 * Run: npm run generate:primer
 * Hooks: after extension version bump; prepackage:extension
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { execSync } from 'child_process';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');

function walkJsFiles(dir, acc = []) {
  if (!fs.existsSync(dir)) return acc;
  for (const ent of fs.readdirSync(dir, { withFileTypes: true })) {
    const p = path.join(dir, ent.name);
    if (ent.isDirectory()) walkJsFiles(p, acc);
    else if (ent.isFile() && ent.name.endsWith('.js')) acc.push(p);
  }
  return acc;
}

function toPosix(p) {
  return path.relative(root, p).split(path.sep).join('/');
}

const manifestPath = path.join(root, 'manifest.json');
const pkgPath = path.join(root, 'package.json');
const manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf8'));
const pkg = JSON.parse(fs.readFileSync(pkgPath, 'utf8'));

const extVer = String(manifest.version || '0.0.0').trim();
const srvVer = String(pkg.version || '0.0.0').trim();
const minChrome = manifest.minimum_chrome_version || '—';
const permCount = Array.isArray(manifest.permissions) ? manifest.permissions.length : 0;
const hostPermCount = Array.isArray(manifest.host_permissions) ? manifest.host_permissions.length : 0;
const matchPatterns = manifest.content_scripts?.[0]?.matches;
const matchCount = Array.isArray(matchPatterns) ? matchPatterns.length : 0;

let storeVer = '';
try {
  storeVer = fs.readFileSync(path.join(root, 'public/install/playshare-extension.version'), 'utf8').trim();
} catch {
  storeVer = '—';
}

let gitSha = '';
try {
  gitSha = execSync('git rev-parse --short HEAD', { cwd: root, encoding: 'utf8' }).trim();
} catch {
  gitSha = '';
}

const contentSrcRoot = path.join(root, 'content', 'src');
const contentFiles = walkJsFiles(contentSrcRoot)
  .map(toPosix)
  .filter((f) => f.startsWith('content/src/'))
  .sort();

const serverDir = path.join(root, 'server');
const serverFiles = fs.existsSync(serverDir)
  ? fs
      .readdirSync(serverDir, { withFileTypes: true })
      .filter((e) => e.isFile() && e.name.endsWith('.js'))
      .map((e) => `server/${e.name}`)
      .sort()
  : [];

const generatedAt = new Date().toISOString();

const meta = {
  generatedAt,
  extensionVersion: extVer,
  serverPackageVersion: srvVer,
  serverPackageName: pkg.name || 'playshare-server',
  gitSha: gitSha || null,
  contentSrcFileCount: contentFiles.length,
  serverModuleCount: serverFiles.length
};

const lines = [
  '## Release & codebase snapshot (auto-generated)',
  '',
  '_This block is regenerated by `npm run generate:primer`. It also runs automatically after `npm run bump:extension` and before `npm run package:extension`, so the diagnostic AI sees the current manifest version and file inventory after you ship changes._',
  '',
  '| | |',
  '|---|-----|',
  `| **Extension version** (\`manifest.json\`) | **${extVer}** |`,
  `| **Minimum Chrome** | ${minChrome} |`,
  `| **Server npm package** | \`${meta.serverPackageName}@${srvVer}\` |`,
  `| **Store / web build id** (\`public/install/playshare-extension.version\`) | ${storeVer} |`,
  `| **Manifest permissions** (declared) | ${permCount} · **host_permissions** ${hostPermCount} · **content match patterns** ${matchCount} |`,
  `| **Generated (UTC)** | ${generatedAt} |`,
  `| **Git revision** | ${gitSha ? `\`${gitSha}\`` : '_not a git checkout / git unavailable_'} |`,
  '',
  '### Content script modules (`content/src/**/*.js`)',
  '',
  ...contentFiles.map((f) => `- \`${f}\``),
  '',
  '### Server modules (`server/*.js`)',
  '',
  ...serverFiles.map((f) => `- \`${f}\``),
  '',
  'When making recommendations, **treat new or unexpected paths above as signals of recent implementation work** that may not yet be described in the narrative primer section.'
];

const autoMdPath = path.join(root, 'server', 'playshare-extension-primer.auto.md');
const metaPath = path.join(root, 'server', 'playshare-extension-primer.meta.json');

fs.writeFileSync(autoMdPath, `${lines.join('\n')}\n`, 'utf8');
fs.writeFileSync(metaPath, `${JSON.stringify(meta, null, 2)}\n`, 'utf8');

console.log(`[generate:primer] wrote ${path.relative(root, autoMdPath)} + meta (${extVer} / ${srvVer})`);
